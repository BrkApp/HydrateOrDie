void main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Firebase with mock config
  await Firebase.initializeApp(options: DefaultFirebaseOptions.currentPlatform);

  runApp(const ProviderScope(child: MyApp()));
}

